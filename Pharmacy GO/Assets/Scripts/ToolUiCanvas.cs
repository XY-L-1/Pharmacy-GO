using UnityEngine;

public class ToolUiCanvas : MonoBehaviour
{

    // Ensures the ToolUI canvas is not duplicated nor destroyed

    private static ToolUiCanvas instance;
    // Start is called once before the first execution of Update after the MonoBehaviour is created
    void Start()
    {
        
    }

    // Update is called once per frame
    void Update()
    {
        
    }
    void Awake()
    {
        if (instance == null)
        {
            instance = this;
            DontDestroyOnLoad(gameObject);
        }
        else
        {
            Destroy(gameObject);
        }
    }
}
